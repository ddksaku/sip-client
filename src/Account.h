#ifndef __Account__
#define	__Account__

/**
 * Account.
 */
class Account {

    private :
        QString title;      /**< Account title. */
        QString username;   /**< Username. */
        QString password;   /**< Password. */
        QString registrar;  /**< Registrar. */
        
    public :
        /** Constructor. */
        Account()
        {}
        
        /** Constructor. */
        Account(const QString &title, const QString &username, const QString &password, const QString &registrar) 
            : title(title), username(username), password(password), registrar(registrar)
        {}
        
        /** Returns account title. */
        QString getTitle() const { return title; }
        
        /** Sets account title. */
        void setTitle(const QString &title) { this->title = title; }
        
        /** Returns username. */
        QString getUsername() const { return username; }
        
        /** Sets username. */
        void setUsername(const QString &username) { this->username = username; }
        
        /** Returns password. */
        QString getPassword() const { return password; }
        
        /** Sets password. */
        void setPassword(const QString &password) { this->password = password; }
        
        /** Returns registrar. */
        QString getRegistrar() const { return registrar; }
        
        /** Sets registrar. */
        void setRegistrar(const QString &registrar) { this->registrar = registrar; }
        
};

#endif	/* __Account__ */
